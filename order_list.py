def sort_numbers(numbers):
    return sorted(numbers)
#The function sort_numbers() takes a list of numbers as an argument and returns the sorted list using the sorted() function. This function returns a new list that contains the same numbers as the original list, but in ascending order
numbers = [3, 6, 1, 8, 2, 10]
sorted_numbers = sort_numbers(numbers)
print(sorted_numbers)
